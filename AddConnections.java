package Connections;

public class AddConnections {
    String ConnName;
    String ConnCity;
    Double ConnSalary;

    public String getConnName() {
        return ConnName;
    }

    public void setConnName(String connName) {
        ConnName = connName;
    }

    public String getConnCity() {
        return ConnCity;
    }

    public void setConnCity(String connCity) {
        ConnCity = connCity;
    }

    public Double getConnSalary() {
        return ConnSalary;
    }

    public void setConnSalary(Double connSalary) {
        ConnSalary = connSalary;
    }


}
